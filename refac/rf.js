function makeUl(arr) {
  let stringHTML = ''
  for (let i = 0; i < arr.length; i++) {
    stringHTML += makeLi(arr[i].arr, i, arr[i].id)
  }
  return stringHTML
}

function makeLi(test, i, testId) {
  let string = `
<li class="testContainer" id="${testId}">`
  let header = test[0].split('\n')
  if (header.length === 1) {
    string += `<h2>
    ${i + 1}. ${header[0]}
  </h2>`
  }
  else {
    string += `<h2 class="paddingLeft">
    ${header[0]}
  </h2>`
    string += `<h2 class="noPaddingTop">
    ${i + 1}. ${header[1]}
  </h2>`
  }
  for (let j = 1; j < test.length; j++) {
    string += `
  <div class="answerContainer focus hover" id="${testId}_${j - 1}" data-value="${j - 1}" tabindex="0">
    <span class="answerSpan answerLetter">${getLetter(j - 1)}) </span>
    <span class="answerSpan answerText">${test[j]}</span>
    <span class="answerSpan answerMark"></span>
  </div>`
  }
  if (BASE.state[testId].type !== 'single') {
    string += `
  <div class="buttonContainer">
    <button class="answer" id="b_${i}">Ответить</button>
  </div>`
  }
  string += `
</li>`
  return string
}

