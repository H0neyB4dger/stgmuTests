function getTestArr(ul) {
  const children = Array.from(ul.children)
  return children.filter(v => v.classList.contains('testContainer'))
}

function getStringArr(arr) {
  return arr.map(el => el.textContent)
}

function prepareArr(arr) {
  return arr.map(v => v.trim().replace(/\n/g, '').replace(/ +/g, ' ').split(/\s(?=\w\))/g))
}

function makeUl(arr) {
  let stringHTML = ''
  for (let i = 0; i < arr.length; i++) {
    stringHTML += makeLi(arr[i], i)
  }
  return stringHTML
}

function makeLi(test, i) {
  let string = `
<li class="testContainer" id="${sectionName}_${i}">
  <h2>
    ${test[0]}
  </h2>`
  for (let j = 1; j < test.length; j++) {
    const letText = test[j].split(/(?<=\w\)) /g)
    string += `
  <div class="answerContainer focus hover" id="${sectionName}_${i}_${j - 1}" data-value="${j - 1}" tabindex="0">
    <span class="answerSpan answerLetter">${letText[0]} </span>
    <span class="answerSpan answerText">${letText[1]}</span>
    <span class="answerSpan answerMark"></span>
  </div>`
  }
  string += `
</li>`
return string
}

function getAnswerObj(arr) {
  const obj = {}
  for (let i = 0; i < arr.length; i++) {
    const innerHTML = arr[i].innerHTML
    const match = innerHTML.match(/(?<=value=")\d/g)
    const type = match.reduce((t, v) => t + +v, 0) === 1? 'single': 'many'
    const answer = type === 'single'? match.indexOf('1'): match.map((v, i) => v === '1'? i: -1).filter(v => v >= 0)
    obj[sectionName + `_${i}`] = {type, answer}
  }
  return obj
}

